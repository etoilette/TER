@dataclass
class csg:
    """
    The controller class.

    Attributes:
        twa: The automaton.
        states_bdds: The BDDs of the states.
        edges_concsets: The concurrent sets of the edges.
        outputs: The output APs.
        current_state: The current state of the controller, always initialized at 0.
    """
    twa: spot.twa_graph
    states_bdds: Dict[int,buddy.bdd]
    edges_concsets: Dict[int, Dict[str, int]]
    outputs: Set[int]

    current_state = 0
    
    def step(self, inputs_val: Dict[int, bool]):
        """
        Makes one transition of the controller: it changes the inner state of the controller following a transition.

        Args:
            inputs_val: A valuation of AP where the values of input APs are taken for the transition of the controller.

        Returns:
            A valuation of AP where the values of outputs APs are generated by the transition.
        """
        direction_step = self.get_a_paretto_max_edge(inputs_val)
        if direction_step is not None:
            self.current_state = direction_step[1]
            return direction_step[0]
        return compute_outputs(self.states_bdds[self.current_state], self.outputs)
    # ici on va devoir reecrire la fonction step qui permet de passer dun etat a un autre grace aux transitions
    #il faut faire attention lorsquon a les inputs qui ne sont plus valides et quon va devoir terminer des transitions
    # quelle transition se finit, pour ca on reprend get_a_paretto_max_edge qui sert a choisir la prochaine cellule / la transition a terminer
    def get_a_paretto_max_edge(self, inputs_val: Dict[int, bool]):
        """
        Choose an edge coherent with inputs and that is maximal, i.e. there is not another edge coherent with the input
        that corresponds to a bigger multiset of transition.

        Args:
            inputs_val: A valuation of AP where the values of input APs are taken for the transition of the controller.

        Returns:
            The output generated by the edge and the destination state.
        """
        resu_edge = None
        for edge in self.twa.out(self.current_state):
            if satisfies_bdd(inputs_val, edge.cond, self.outputs) and (resu_edge is None or leq_mset(self.edges_concsets[self.twa.edge_number(resu_edge)], self.edges_concsets[self.twa.edge_number(edge)])):
                resu_edge = edge
        if resu_edge is not None:
            return (compute_outputs(resu_edge.cond, self.outputs), resu_edge.dst)

def satisfies_bdd (valuation: Dict[int, bool], bdd: buddy.bdd, outputs_set: Set[int]):
    """
    Args:
        valuation: A valuation of the AP.
        bdd: A BDD.
        outputs_list: Outputs APs.

    Returns:
        A Boolean indicating whether the valuation satisfies the input part of the bdd.
    """
    if bdd == buddy.bddfalse:
        return False
    if bdd == buddy.bddtrue:
        return True
    top = buddy.bdd_var(bdd)
    low = buddy.bdd_low(bdd)
    high = buddy.bdd_high(bdd)
    if top in outputs_set:
        return satisfies_bdd(valuation, low, outputs_set) or satisfies_bdd(valuation, high, outputs_set)
    if valuation[top]:
        return satisfies_bdd(valuation, high, outputs_set)
    else:
        return satisfies_bdd(valuation, low, outputs_set)

def compute_outputs(bdd: buddy.bdd, outputs: set[int]):
    """
    Extract a compatible valuation for the output APs from the given cube
    over input and output APs. 
    Args:
        bdd: Input cube.
        outputs: bdd_vars of output APs as a set.

    Returns:
        The valuation of output APs according to the bdd.
    """
    resu = {ap: False for ap in outputs}
    bdd_temp = bdd
    while bdd_temp != buddy.bddtrue:
        top = buddy.bdd_var(bdd_temp)
        low = buddy.bdd_low(bdd_temp)
        high = buddy.bdd_high(bdd_temp)
        if low == buddy.bddfalse:
            if top in outputs:
                resu[top] = True
            bdd_temp = high
            continue
        if high == buddy.bddfalse:
            if top in outputs:
                resu[top] = False
            bdd_temp = low
            continue
        raise Exception("Not a cube!")
    return resu

