def diff_mset(mset1: Dict[int,int], mset2: Dict[int,int]):
    """
    Compute the difference mset1 - mset2.
    
    Args:
        mset1: The smaller multiset.
        mset2: The bigger multiset.
    
    Returns:
        The difference multiset.
    """
    resu = dict()
    for key in mset1.keys() & mset2.keys():
        value = mset1[key] - mset2[key]
        if value > 0:
            resu[key] = value
    for key in set(mset1.keys()).difference(mset2.keys()):
        resu[key] = mset1[key]
    return resu

def leq_mset(mset1: Dict[int,int], mset2: Dict[int,int]):
    """
    Predicate whether mset1 <= mset2.
    
    Args:
        mset1: The multiset that is checked to be smaller.
        mset2: The multiset that is checked to be bigger.
    
    Returns:
        The Boolean whether mset1 <= mset2.
    """
    return not any(key not in mset2.keys() or mset1[key] > mset2[key] for key in mset1.keys())
 
def sub_multi_set(mset: Dict[int,int]):
    """
    Generate all sub-multisets of mset.
    
    Args:
        mset: The multiset.

    Returns:
        The generator of the submultisets of mset.
    """
    return sub_multi_set_run(mset, set(mset.keys()))

def sub_multi_set_run(mset: Dict[int,int], keys: Set[int]):
    """
    The internal function of sub_multi_set. Computes all the submultisets of mset on the keys keys.
    
    Args:
        mset: The multiset.
        keys: The keys.
    
    Returns:
        The generator of the submultisets on keys.
    """
    if len(keys) == 0:
        yield dict()
    else:
        key = keys.pop()
        for sub_mset in sub_multi_set_run(mset, keys):
            for i in range(mset[key]+1):
                if i != 0:
                    sub_mset[key] = i
                elif key in sub_mset.keys():
                    del sub_mset[key]
                yield sub_mset.copy()
        keys.add(key)

