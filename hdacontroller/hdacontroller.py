def get_literal(strg: str, ap: Dict[str, buddy.bdd]):
    """
    Get the ap of a literal.
    
    Args:
        strg: The string of the litteral.
        ap: The map from strings of atomic propositions to their buddy representation.
    
    Returns:
        The bdd of the litteral.
    """
    if strg[0] == "!":
        return buddy.bdd_not(ap[strg[1:]])
    else:
        return ap[strg]

def get_string_bdd(strg: str, ap: Dict[str, buddy.bdd]):
    """
    Builds a bdd for the string strg representing a formula in conjunctive normal form over APs ap.
    
    Args:
        strg: The text of the formula.
        ap: The map from strings of atomic propositions to their buddy representation.
    
    Returns:
        The bdd of the formula.
    """
    clauses = strg.replace(" ","").split('&')
    resu = buddy.bddtrue
    for clause in clauses:
        literals = clause.split('|')
        resu_clause = buddy.bddfalse
        for literal in literals:
            resu_clause = resu_clause | get_literal(literal, ap)
        resu = resu & resu_clause
    return resu

def get_marking_output(marking: Tuple[int], pn: ipetrinet, ap: Dict[str, buddy.bdd]):
    """
    Builds the bdd for the output of a marking.
    
    Args:
        marking: The marking.
        pn: The ambiant Petri net.
        ap: The map from strings of atomic propositions to their buddy representation.
    
    Retruns:
        The bdd for the output of the marking."""
    resu = buddy.bddtrue
    for state in range(len(marking)):
        try:
            if marking[state] > 0:
                resu = resu & get_string_bdd(pn.placesOuts[state], ap) # psc normally try except should be faster
        except Exception:
            continue
    return resu

