def get_cell_output(cell: maxCell, pn: ipetrinet, ap: dict):
    """
    Builds the bdd for the active inputs / outputs of a MaxCell.
    
    Args:
        cell: The MaxCell in the HDA.
        pn: The ambiant Petri net.
        ap: The map from strings of atomic propositions to their buddy representation.
    
    Returns:
        The bdds for the inputs and outputs of the cell.
    """

    resu = buddy.bddtrue
    #on parcourt les outputs des transitions en cours dans cette cellule
    for transition in cell.concset.keys():
        t = pn.transitions[transition]
        #si la transition a des inputs, on rajoute dans les inputs
        if t.inputs:
            resu = resu & get_string_bdd(t.inputs, ap)
        #si la transition a des outputs, on rajoute dans les outputs
        if t.outputs:
            resu = resu & get_string_bdd(t.outputs, ap)
    # on parcourt les places pour regarder le marking 
    for place in range(len(cell.marking)):
        tokens = cell.marking[place]
        # si la place est marquee et definie des outputs on rajoute aussi
        if tokens > 0 and place in pn.placesOuts:
            resu = resu & get_string_bdd(pn.placesOuts[place], ap)
    return resu

def get_edge_output(pn_transitions: dict, pn: ipetrinet, ap: dict, cell: maxCell):
    """
    Builds the bdd for the inputs and outputs of an edge.
    
    Args:
        pn_transitions: The multiset of Petri net transitions starting or terminating on the edge.
        pn: The ambiant Petri net.
        ap: The map from strings of atomic propositions to their buddy representation.
        cell: The MaxCell of the edge.
    
    Returns:
        The bdd for the inputs and outputs of the edge.
    """
    # on part des outputs de la cellule de depart
    # on separe les inputs dans un dictionnaire et les outputs de lautre
    resu = get_cell_output(cell, pn, ap)
    #on parcourt les transitions du pn actifs
    for trans in pn_transitions.keys():
        t = pn.transitions[trans]
        #on rajoute les conditions des inputs sil y en a
        if t.inputs:
            bdd_in = get_string_bdd(t.inputs, ap)
            resu = resu & bdd_in
            # on verifie que l ap reste possible sinon erreur
            if resu == buddy.bddfalse:
                raise Inputs_Error
        #on rajoute les outputs sil y en a aussi
        if t.outputs:
            bdd_out = get_string_bdd(t.outputs, ap)
            resu = resu & bdd_out
    return resu

def get_state(marking: Tuple[int], states: Dict[int, Tuple[int]], graph: spot.twa_graph, pn: ipetrinet, ap: Dict[str, buddy.bdd], states_bdds: Dict[int, buddy.bdd]):
    """
    The state of the marking. Builds a new state in the automaton, if necessary.

    Args:
        marking : The marking for the state.
        states: The dictionnary of already existing states.
        graph: The automaton.
        pn: The ambiant Petri net.
        ap: The map from strings of atomic propositions to their buddy representation.
        states_bdd: The map from states to their output bdd.
    
    Returns:
        The state of the marking.
    """
    if marking in states:
        state = states[marking]
    else:
        label = get_marking_output(marking, pn, ap)
        state = graph.new_state()
        states[marking] = state
        states_bdds[state] = label
    return state

def get_state_for_cell(cell_id: int, cell: maxCell, cell_to_state, graph: spot.twa_graph, pn: ipetrinet, ap: Dict[str, buddy.bdd], states_bdds: Dict[int, buddy.bdd]):
    """
    The state of the cell. Builds a new state in the automaton, if necessary

    Args:
        cell_id: The id of the cell
        cell: The cell in the MaxCell HDA
        cell_to_state: The dictionnary of already existing states, mapped by cell id.
        graph: The automaton in construction.
        pn: The ambiant Petri net.
        ap: The map from strings of atomic propositions to their buddy representation.
        states_bdd: The map from states to their output bdd.

    Returns:
        The state of the cell.
    """
    #on verifie si on a pas deja un etat cree pour la cellule
    if cell_id in cell_to_state:
        return cell_to_state[cell_id]
    #on calcule le bdd representant les outputs actifs de la cellule
    bdds = get_cell_output(cell, pn, ap)
    #on cree un nouvel etat au graphe quon enregistre dans notre dictionnaire pour la cellule
    state = graph.new_state()
    #on note dans cell_to_state que l etat associe a la cellule est celle ci
    cell_to_state[cell_id] = state
    #on note les outputs 
    states_bdds[state] = bdds
    return state

class Inputs_Error (Exception):
    """
    If a bdd for the inputs of a transition is false, the transition is not added to the automaton.
    """
    pass

class Outputs_Error (Exception):
    """
    If a bdd for the outputs is false, then, this error is raised.
    """
    def __init__(self, message):            
        super().__init__(message)

def get_io(marking_source: Tuple[int], concset: Dict[int,int], pn: ipetrinet, ap: Dict[str, buddy.bdd]):
    """
    Gets the input/output bdd when firing concset from marking_source.

    Args:
        marking_source: The initial marking.
        concset: The multiset of transitions.
        pn: The ambiant Petri net.
        ap: The map from strings of atomic propositions to their buddy representation.
    
    Returns:
        The bdd for the input/outputs of firing concset from marking_source.
    """
    resu = get_marking_output(marking_source, pn, ap)
    for t in concset.keys():
        inputs = pn.transitions[t].inputs
        outputs = pn.transitions[t].outputs
        if inputs:
            bdd_inputs = get_string_bdd(inputs, ap)
            resu = resu & bdd_inputs
            if resu == buddy.bddfalse:
                raise Inputs_Error
        if outputs:
            bdd_outputs = get_string_bdd(outputs, ap)
            resu = resu & bdd_outputs
            if resu == buddy.bddfalse:
                raise Outputs_Error(f"Incompatible outputs in {marking_source} when firing {concset}")
            resu = resu & get_string_bdd(outputs, ap)
    return resu

def add_transition(graph:spot.twa_graph, 
                   pn: ipetrinet, 
                   ap: Dict[str, buddy.bdd], 
                   states_bdds: Dict[int, buddy.bdd], 
                   edges_ios: Dict[Tuple[Tuple[int]], buddy.bdd], 
                   edges_concsets: Dict[int, Dict[int, int]], 
                   marking_source: Tuple[int], 
                   concset: Dict[int,int], 
                   states: Dict[int, Tuple[int]]):
    """
    Adds a transition in the automaton from the state representing marking_source and following concset. Returns the obtained marking.
    
    Args:
        graph: The automaton in construction.
        pn: The ambiant Petri net.
        ap: The map from strings of atomic propositions to their buddy representation.
        states_bdd: The map from states to their output bdd.
        edges_ios: The map from pair of markings to their list of possible input/output bdd.
        edges_concsets: The map from edges of the automaton to their multiset of transitions.
        marking_source: The initial marking.
        concset: The multiset of transitions.
        states: The map from markings to already existing states.
    
    Returns:
        The marking obtained after firing concset.
    """
    marking_target = compute_marking_transi(marking_source, concset, pn)
    state_source = get_state(marking_source, states, graph, pn, ap, states_bdds)
    state_target = get_state(marking_target, states, graph, pn, ap, states_bdds)
    try:
        label = get_io(marking_source, concset, pn, ap)
    except Inputs_Error:
        return marking_target
    if not (marking_source, marking_target) in edges_ios.keys():
        edge = graph.new_edge(state_source, state_target, label)
        edges_concsets[edge] = concset
        edges_ios[(marking_source, marking_target)] = [label]
    elif not any(existing_label == label for existing_label in edges_ios[(marking_source, marking_target)]):
        edge = graph.new_edge(state_source, state_target, label)
        edges_concsets[graph.edge_number(edge)] = concset
        edges_ios[(marking_source, marking_target)].append(label)
    return marking_target

def add_many_transitions(graph:spot.twa_graph, 
                         pn: ipetrinet, 
                         ap: Dict[str, buddy.bdd], 
                         states_bdds: Dict[int, buddy.bdd], 
                         edges_ios: Dict[Tuple[Tuple[int]], buddy.bdd], 
                         edges_concsets: Dict[int, Dict[int, int]], 
                         marking_source: Tuple[int], 
                         concset: Dict[int,int], 
                         states: Dict[int, Tuple[int]]):
    """
    Adds all the transitions from marking_source in the current cell with concset {concset}.

    Args:
        graph: The automaton in construction.
        pn: The ambiant Petri net.
        ap: The map from strings of atomic propositions to their buddy representation.
        states_bdd: The map from states to their output bdd.
        edges_ios: The map from pair of markings to their list of possible input/output bdd.
        edges_concsets: The map from edges of the automaton to their multiset of transitions.
        marking_source: The initial marking.
        concset: The multiset of transitions.
        states: The map from markings to already existing states.
    """
    for mset in sub_multi_set(concset):
        if len(mset) != 0:
            marking_target = add_transition(graph, pn, ap, states_bdds, edges_ios, edges_concsets, marking_source, mset, states)
            concset_comp = diff_mset(concset, mset)
            add_many_transitions(graph, pn, ap, states_bdds, edges_ios, edges_concsets, marking_target, concset_comp, states)

def hdatocsg (hda: Dict[int, maxCell], pn: ipetrinet):
    """
    Takes an HDA in MAXCELL form that represents the petri net pn in inputs and produces its controller graph associated
    
    Args:
        hda: The HDA.
        pn: The interpreted Petri net.
    
    Returns:
        The Controller Graph semantics of the HDA.
    """
    bdict = spot.make_bdd_dict()
    graph = spot.make_twa_graph(bdict)

    ap = dict()
    for ap_in in pn.inputs:
        ap_var = buddy.bdd_ithvar(graph.register_ap(ap_in))
        ap[ap_in] = ap_var
    
    outputs = set()
    for ap_out in pn.outputs:
        ap_int = graph.register_ap(ap_out)
        ap_var = buddy.bdd_ithvar(ap_int)
        ap[ap_out] = ap_var
        outputs.add(ap_int)
        spot.set_synthesis_outputs(graph, ap_var)

    #states keeps track of the association marking <=> states of the automatons
    # states = dict() on a plus besoin de ce dictionnaire, on se concentre sur les transitions et non plus sur les marquages

    # chaque etat du controller = cellule du hda
    cell_to_state = dict()

    #edges_ios keeps track of the association edges <=> inputs/outputs
    edges_ios = dict()

    #edges_concsets stores the concset corresponding to each edge
    edges_concsets = dict()

    #states_bdds stores the bdd of inputs/outputs of each state
    states_bdds = dict()

    # on cree directement les etats comme on a deja le hda construit par pn2MAXHDA
    for cell_id, cell in hda.items():
        #on appelle la fonction get_state_for_cell qui creera un nouvel etat du graphe 
        # si necessaire (si pas deja cree, en theorie non) et lui associera la cellule
        get_state_for_cell(cell_id, cell, cell_to_state, graph, pn, ap, states_bdds)

    #on cherche dans le hda la cellule initiale, on prend juste l id car on a deja cree tous les etats correspondant de l hda 
    for cell_id, cell in hda.items():
        if cell.marking == pn.initial and not cell.concset:
            initial_id = cell_id

    initial_state = cell_to_state[initial_id]
    graph.set_init_state(initial_state)

    #on cree les transitions du graphe
    for cell_id, cell in hda.items():
        state = cell_to_state[cell_id]
        #on prend l id de la cellule suivante et les transitions vers elle
        for next_id, hda_edges in cell.transitions.items():
            next_state = cell_to_state[next_id]

            # on parcourt chaque transition (quand on passe d unne cellule a l autre) du hda 
            for hda_edge in hda_edges:
                active = {}
                
                # on lit les transitions actives qui commencent ou terminent
                for t, n in hda_edge.get('start', {}).items():
                    active[t] = n
                for t, n in hda_edge.get('terminate', {}).items():
                    active[t] = n

                # on calcule le bdd output input
                bdd = get_edge_output(active, pn, ap, cell)
                # on rajoute la transition dans le graphe
                edge = graph.new_edge(state, next_state, bdd)
                #on mémorise le concset pour le controller
                edges_concsets[edge] = active
    #for maxcell in hda.keys():
    #    concset = hda[maxcell].concset
    #    marking = compute_marking_pre(hda[maxcell].marking, concset , pn)
    #    add_many_transitions(graph, pn, ap, states_bdds, edges_ios, edges_concsets, marking, concset, states)

    return csg(graph, states_bdds, edges_concsets, outputs)

def build_csg_from_files(path_to_pn2hda, pnml_file, io_file):
    """
    Builds the Concurrent Step Graph semantics for the interpreted Petri net specified by the pnml file and the i/o file.
    
    Args:
        path_to_pn2hda: The path to directory containing the pn2HDA and pn2MAXHDA executables. 
        pnml_file: The file containing the Petri net in pnml format.
        io_file: The file specifying the inputs/outputs of the iPN.
    
    Returns:
        The Concurrent Step Graph semantics for the interpreted Petri net specified by the pnml file and the i/o file.
    """
    if path_to_pn2hda is None:
        main_dir = Path(__file__).parent.parent
        matches_pn2HDA = glob.glob(str(main_dir / "*" / "pn2HDA"))
        matches_pn2MAXHDA = glob.glob(str(main_dir / "*" / "pn2MAXHDA"))
    else:
        main_dir = Path(path_to_pn2hda)
        matches_pn2HDA = glob.glob(str(main_dir / "pn2HDA"))
        matches_pn2MAXHDA = glob.glob(str(main_dir / "pn2MAXHDA"))
    if not matches_pn2HDA:
        raise FileNotFoundError("Could not find the C++ executable 'pn2HDA' in pn2HDA/*/")
    pn2HDA = matches_pn2HDA[0]
    pnfetcher = subprocess.run(
    [pn2HDA,"standard", pnml_file, "base"],
    capture_output=True,
    text=True 
    )
    pn_text = pnfetcher.stdout

    if not matches_pn2MAXHDA:
        raise FileNotFoundError("Could not find the C++ executable 'pn2MAXHDA' in pn2HDA/*/")
    pn2MAXHDA = matches_pn2MAXHDA[0]
    hdafetcher = subprocess.run(
    [pn2MAXHDA,"standard", pnml_file, "str"],
    capture_output=True,
    text=True 
    )
    hda_text = hdafetcher.stdout
    
    with open(args.io_file) as io_file:
        pn = parse_petri_net(pn_text)
        hda = parse_maxcell_hda(hda_text, pn)
        ipn = parse_io(pn, io_file.read())
    return hdatocsg(hda, ipn)

